main(List<String> args) {
  
  // If and Else Statements
  bool isOn = false;

  if(!isOn) {
    print("Not working");
  }
  else {
    print("Working");
  }

  // Switch Statements
  int test = 2;
  switch (test) {
    case 0:
      print("test is zero");
      break;
    case 1: 
      print("test is one");
      break;
    default:
      print("default");
  }
}